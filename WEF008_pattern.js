const pattern2DArrLib = {}


for (let patternStringItemKey in patternStringLib) {

    pattern2DArrLib[patternStringItemKey] = transformPatternStrToArr(patternStringLib[patternStringItemKey])

}



function transformPatternStrToArr(inputPattern) {
    let patternStrArr = inputPattern.split('\n')
    patternStrArr.pop()
    patternStrArr.shift()

    let pattern2dArr = []

    for (let i = 0; i < patternStrArr.length; i++) {
        pattern2dArr[i] = [];
    }

    for (let i = 0; i < patternStrArr.length; i++) {    //row
        for (let j = 0; j < patternStrArr[i].length; j++) {  //col
            if (patternStrArr[i][j] === '.') {
                pattern2dArr[i][j] = 0
            } else {
                pattern2dArr[i][j] = 1
            }

        }
    }

    return pattern2dArr

}


